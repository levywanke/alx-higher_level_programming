#!/usr/bin/python3

def add(a, b):
    """I add funcs

    arguments:
        a: fs int
        b: sec int

    return:
        a + b
    """
    return (a + b)


def sub(a, b):
    """I sub funcs

    arguments:
        a: fs int
        b: sec int

    return:
        a - b
    """
    return (a - b)


def mul(a, b):
    """I mult funcs

    arguments:
        a: fs int
        b: sec int

    return:
        a * b
    """
    return (a * b)


def div(a, b):
    """I div funcs

    arguments:
        a: fs int
        b: sec int

    return:
        a / b
    """
    return int(a / b)
